package MyProject;

public class Calculator {
    public void add(int num1, int num2){
        System.out.println(num1 + num2);
    }

    public void sub(int num1, int num2){
        System.out.println(num1 - num2);
    }

    public void mul(int num1, int num2){
        System.out.println(num1 * num2);
    }

    public void iMul(int num1, int num2){
        System.out.println(num1 / num2);
    }
}
